package com.esprit.mycitymystory.model;

/**
 * Model used in MenuDrawerFragment for displaying menu items
 *
 * @author Syrine Dridi
 */
public class MenuItemModel {
    String title;
    int icon;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }
}
