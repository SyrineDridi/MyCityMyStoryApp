package com.esprit.mycitymystory.model;

/**
 * Model used in HomeFragment for displaying horizontal items
 *
 * @author Syrine Dridi
 */
public class HorizontalGroupModel {
    String id, image, title;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
